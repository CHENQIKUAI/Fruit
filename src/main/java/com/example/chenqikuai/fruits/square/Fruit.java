package com.example.chenqikuai.fruits.square;

public class Fruit {
    private String name;
    private String imageURL;

    public Fruit(String name, String imageURL) {
        this.name = name;
        this.imageURL = imageURL;
    }

    public String getName() {
        return name;
    }

    public String getImageURL() {
        return imageURL;
    }
}


